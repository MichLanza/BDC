package Model;

/**
 *
 * @author Michele Lanza
 */
public class Estadistica {
    public String x;
    public int y;
    
    public Estadistica() {
    }
    
    
    public Estadistica(String x, int y){
        this.x = x;
        this.y = y;
    }

    public String getX() {
        return x;
    }

    public void setX(String x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
    
    
    
}
